#import "UIColor+PetHealth.h"

@implementation UIColor (PetHealth)

+ (UIColor *)purinaRed
{
    return [UIColor colorWithRed:215.0f/255.0f green:6.0f/255.f blue:6.0f/255.0f alpha:1.0f];
}


+ (UIColor *)purinaLightGrey
{
    return [UIColor colorWithRed:173/255.f green:173/255.f blue:173/255.f alpha:255/255.f];
}


+ (UIColor *)purinaDarkGrey
{
    return [UIColor colorWithRed:86/255.f green:86/255.f blue:86/255.f alpha:255/255.f];
}


@end
